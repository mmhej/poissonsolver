void class_pencil::fft_shift( )
{
//----------------------------------------------------------------------------//
// Parameters
//----------------------------------------------------------------------------//


//----------------------------------------------------------------------------//
// Local variables
//----------------------------------------------------------------------------//
	int i;

//----------------------------------------------------------------------------//
// Objects
//----------------------------------------------------------------------------//

//----------------------------------------------------------------------------//
// Set fft size
//----------------------------------------------------------------------------//

//----------------------------------------------------------------------------//
// Re-size fft vectors
//----------------------------------------------------------------------------//
	for(i = 0; i < nfft/2; ++i)
	{
		if( bX )
		{
			auxX[i] = X[i];
			X[i] = X[i+nfft/2];
		}
		if( bY )
		{
			auxY[i] = Y[i];
			Y[i] = Y[i+nfft/2];
		}
		if( bZ )
		{
			auxZ[i] = Z[i];
			Z[i] = Z[i+nfft/2];
		}
	}

	for(i = nfft/2; i < nfft; ++i)
	{
		if( bX )
		{
			X[i] = auxX[i-nfft/2];
		}
		if( bY )
		{
			Y[i] = auxY[i-nfft/2];
		}
		if( bZ )
		{
			Z[i] = auxZ[i-nfft/2];
		}
	}


//----------------------------------------------------------------------------//
// Return
//----------------------------------------------------------------------------//
	return;
}
