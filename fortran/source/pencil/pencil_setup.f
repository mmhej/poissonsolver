SUBROUTINE pencil_setup( pen, inX, inY, inZ )

IMPLICIT NONE

!------------------------------------------------------------------------------!
! Arguments
!------------------------------------------------------------------------------!
	TYPE(class_pencil), INTENT(INOUT) :: pen
	LOGICAL, INTENT(IN) :: inX
	LOGICAL, INTENT(IN) :: inY
	LOGICAL, INTENT(IN) :: inZ

!------------------------------------------------------------------------------!
! Set toggles
!------------------------------------------------------------------------------!
	pen%bX = inX;
	pen%bY = inY;
	pen%bZ = inZ;

!------------------------------------------------------------------------------!
! Return
!------------------------------------------------------------------------------!
9999 CONTINUE

RETURN
END SUBROUTINE pencil_setup
